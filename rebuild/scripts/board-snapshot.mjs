#!/usr/bin/env node
// Distill a vitest --reporter=json run of the ported board into a stable,
// diffable per-test snapshot (test-workers/ported/BOARD-SNAPSHOT.txt).
//
// Why: the regression gate for the ported board is "no previously-passing
// test name flips", checked by DIFFING two snapshots — not by re-running the
// suite. Full board runs are periodic checkpoints (see COVERAGE.md header);
// each checkpoint refreshes this file in the same commit.
//
// Usage: node scripts/board-snapshot.mjs <vitest-json> [out-file]
//   vitest run --config vitest.ported.config.ts --reporter=json --outputFile=board.json
//   node scripts/board-snapshot.mjs board.json

import { readFileSync, writeFileSync } from "node:fs";

const [jsonPath, outPath = "test-workers/ported/BOARD-SNAPSHOT.txt"] = process.argv.slice(2);
if (!jsonPath) {
  console.error("usage: node scripts/board-snapshot.mjs <vitest-json> [out-file]");
  process.exit(1);
}

const report = JSON.parse(readFileSync(jsonPath, "utf8"));
const lines = [];
for (const file of report.testResults ?? []) {
  // Snapshots must diff cleanly across checkouts/worktrees: anchor the path
  // at the suite root instead of making it cwd-relative.
  const abs = file.name.replaceAll("\\", "/");
  const anchor = abs.indexOf("test-workers/");
  const rel = anchor >= 0 ? abs.slice(anchor) : abs;
  for (const t of file.assertionResults ?? []) {
    // status: passed | failed | pending (skipped) | todo
    lines.push(`${t.status.toUpperCase().padEnd(7)} ${rel} :: ${t.fullName}`);
  }
}
lines.sort();

const passed = lines.filter((l) => l.startsWith("PASSED")).length;
const failed = lines.filter((l) => l.startsWith("FAILED")).length;
const other = lines.length - passed - failed;
const header = [
  `# Ported board snapshot — generated by scripts/board-snapshot.mjs; never hand-edit.`,
  `# Gate: diff against the previous snapshot — no PASSED line may flip away.`,
  `# totals: ${lines.length} tests · ${passed} passed · ${failed} failed · ${other} skipped/todo`,
];

writeFileSync(outPath, [...header, ...lines, ""].join("\n"));
console.log(`${outPath}: ${lines.length} tests · ${passed} passed · ${failed} failed · ${other} other`);
